function mouseOver() { document.getElementById("demo").style.color = "red";
}
function mouseOut() {
document.getElementById("demo").style.color = "black";
}
function bigImg(x) {
  x.style.height = "140px";
  x.style.width = "140px";
}

function normalImg(x) {
  x.style.height = "32px";
  x.style.width = "32px";
}